def generate_hosts_content(machines: Dict[str, Dict[str, Any]]) -> str:
    """
    Generate /etc/hosts content from machines information.
    
    Args:
        machines: Dictionary of machine configurations
        
    Returns:
        String containing /etc/hosts content
    """
    lines = [
        "# /etc/hosts - Generated by automation",
        "# Standard localhost entries",
        "127.0.0.1   localhost localhost.localdomain localhost4 localhost4.localdomain4",
        "::1         localhost localhost.localdomain localhost6 localhost6.localdomain6",
        "",
        "# Machine entries from deployment",
    ]
    
    for machine_name, machine_info in sorted(machines.items()):
        private_ip = machine_info.get('private_ip', '')
        if not private_ip:
            continue
        
        # Add entry with short name and demo.guardium domain
        short_name = machine_name
        fqdn = f"{machine_name}.demo.guardium"
        
        lines.append(f"{private_ip:<15} {fqdn} {short_name}")
    
    lines.append("")  # Empty line at end
    return "\n".join(lines)


def update_hosts_file_local(hosts_content: str, logger) -> bool:
    """
    Update /etc/hosts file on local machine.
    
    Args:
        hosts_content: Content to write to /etc/hosts
        logger: Logger instance
        
    Returns:
        True if successful, False otherwise
    """
    try:
        # Backup existing /etc/hosts
        logger.info("Backing up existing /etc/hosts")
        result = subprocess.run(
            ["cp", "/etc/hosts", "/etc/hosts.backup"],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.warning(f"Could not backup /etc/hosts: {result.stderr}")
        
        # Write new /etc/hosts content
        logger.info("Writing new /etc/hosts content")
        
        # Create temporary file with new content
        temp_file = "/tmp/hosts.new"
        with open(temp_file, 'w') as f:
            f.write(hosts_content)
        
        # Move temporary file to /etc/hosts
        logger.info("Installing new /etc/hosts")
        result = subprocess.run(
            ["mv", temp_file, "/etc/hosts"],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.error(f"Failed to install /etc/hosts: {result.stderr}")
            return False
        
        # Set proper permissions
        result = subprocess.run(
            ["chmod", "644", "/etc/hosts"],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.warning(f"Could not set permissions on /etc/hosts: {result.stderr}")
        
        logger.info("Successfully updated /etc/hosts")
        return True
        
    except Exception as e:
        logger.error(f"Error updating /etc/hosts: {str(e)}")
        return False


def update_hosts_file_remote(ssh_client: SSHClient, hosts_content: str, logger) -> bool:
    """
    Update /etc/hosts file on remote machine via SSH using sudo.
    """
    try:
        logger.info("Backing up existing /etc/hosts")
        backup_result = ssh_client.execute_command("sudo cp /etc/hosts /etc/hosts.backup", print_output=False)
        if backup_result['rc'] != 0:
            logger.warning(f"Could not backup /etc/hosts: {backup_result['stderr']}")
        
        logger.info("Writing new /etc/hosts content")
        temp_file = "/tmp/hosts.new"
        write_cmd = f"cat > {temp_file} << 'EOF'\n{hosts_content}\nEOF"
        
        result = ssh_client.execute_command(write_cmd, print_output=False)
        if result['rc'] != 0:
            logger.error(f"Failed to write temporary hosts file: {result['stderr']}")
            return False
        
        logger.info("Installing new /etc/hosts")
        result = ssh_client.execute_command(f"sudo mv {temp_file} /etc/hosts", print_output=False)
        if result['rc'] != 0:
            logger.error(f"Failed to install /etc/hosts: {result['stderr']}")
            return False
        
        result = ssh_client.execute_command("sudo chmod 644 /etc/hosts", print_output=False)
        if result['rc'] != 0:
            logger.warning(f"Could not set permissions on /etc/hosts: {result['stderr']}")
        
        logger.info("Successfully updated /etc/hosts")
        return True
        
    except Exception as e:
        logger.error(f"Error updating /etc/hosts: {str(e)}")
        return False


def configure_sshd_local(logger) -> bool:
    """
    Configure SSHD on local machine.
    Changes:
    - PasswordAuthentication yes
    - PermitRootLogin yes
    - LoginGraceTime 0
    
    Args:
        logger: Logger instance
        
    Returns:
        True if successful, False otherwise
    """
    try:
        sshd_config = "/etc/ssh/sshd_config"
        
        logger.info("Modifying SSHD configuration")
        
        # Replace or add PasswordAuthentication setting
        if not modify_config_file(
            path=sshd_config,
            content='PasswordAuthentication yes\n',
            mode='replace',
            pattern=r'^#*\s*PasswordAuthentication',
            backup=True,
            logger=logger
        ):
            # If pattern not found, append it
            modify_config_file(
                path=sshd_config,
                content='\n# Added by automation\nPasswordAuthentication yes\n',
                mode='append',
                backup=False,
                logger=logger
            )
        
        # Replace or add PermitRootLogin setting
        if not modify_config_file(
            path=sshd_config,
            content='PermitRootLogin yes\n',
            mode='replace',
            pattern=r'^#*\s*PermitRootLogin',
            backup=False,
            logger=logger
        ):
            modify_config_file(
                path=sshd_config,
                content='PermitRootLogin yes\n',
                mode='append',
                backup=False,
                logger=logger
            )
        
        # Replace or add LoginGraceTime setting
        if not modify_config_file(
            path=sshd_config,
            content='LoginGraceTime 0\n',
            mode='replace',
            pattern=r'^#*\s*LoginGraceTime',
            backup=False,
            logger=logger
        ):
            modify_config_file(
                path=sshd_config,
                content='LoginGraceTime 0\n',
                mode='append',
                backup=False,
                logger=logger
            )
        
        # Set proper permissions
        result = subprocess.run(
            ["chmod", "600", sshd_config],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.warning(f"Could not set permissions on sshd_config: {result.stderr}")
        
        # Remove cloud-init override config if exists
        cloud_init_config = "/etc/ssh/sshd_config.d/50-cloud-init.conf"
        logger.info(f"Removing {cloud_init_config} if exists")
        result = subprocess.run(
            ["rm", "-f", cloud_init_config],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.warning(f"Could not remove {cloud_init_config}: {result.stderr}")
        
        # Restart SSHD
        logger.info("Restarting SSHD service")
        result = subprocess.run(
            ["systemctl", "restart", "sshd"],
            capture_output=True,
            text=True
        )
        if result.returncode != 0:
            logger.error(f"Failed to restart SSHD: {result.stderr}")
            return False
        
        logger.info("Successfully configured SSHD")
        return True
        
    except Exception as e:
        logger.error(f"Error configuring SSHD: {str(e)}")
        return False


def set_root_password_local(password: str, logger) -> bool:
    """
    Set root password on local machine.
    
    Args:
        password: New root password
        logger: Logger instance
        
    Returns:
        True if successful, False otherwise
    """
    try:
        logger.info("Setting root password on local machine")
        
        # Use chpasswd to set password
        # Format: username:password
        process = subprocess.Popen(
            ["chpasswd"],
            stdin=subprocess.PIPE,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True
        )
        
        stdout, stderr = process.communicate(input=f"root:{password}\n")
        
        if process.returncode != 0:
            logger.error(f"Failed to set root password: {stderr}")
            return False
        
        logger.info("Successfully set root password")
        return True
        
    except Exception as e:
        logger.error(f"Error setting root password: {str(e)}")
        return False


def set_root_password_remote(ssh_client: SSHClient, password: str, logger) -> bool:
    """
    Set root password on remote machine via SSH.
    
    Args:
        ssh_client: SSH client instance
        password: New root password
        logger: Logger instance
        
    Returns:
        True if successful, False otherwise
    """
    try:
        logger.info("Setting root password on remote machine")
        
        # Use chpasswd with sudo
        # We need to pass the password securely through stdin
        cmd = f"echo 'root:{password}' | sudo chpasswd"
        
        result = ssh_client.execute_command(cmd, print_output=False)
        if result['rc'] != 0:
            logger.error(f"Failed to set root password: {result['stderr']}")
            return False
        
        logger.info("Successfully set root password")
        return True
        
    except Exception as e:
        logger.error(f"Error setting root password: {str(e)}")
        return False


