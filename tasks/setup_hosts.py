def configure_sshd_remote(ssh_client: SSHClient, logger) -> bool:
    """
    Configure SSHD on remote machine via SSH using sudo.
    Changes:
    - PasswordAuthentication yes
    - PermitRootLogin yes
    - LoginGraceTime 0
    
    Args:
        ssh_client: SSH client instance
        logger: Logger instance
        
    Returns:
        True if successful, False otherwise
    """
    try:
        sshd_config = "/etc/ssh/sshd_config"
        
        logger.info("Backing up existing sshd_config")
        backup_result = ssh_client.execute_command(f"sudo cp {sshd_config} {sshd_config}.backup", print_output=False)
        if backup_result['rc'] != 0:
            logger.warning(f"Could not backup sshd_config: {backup_result['stderr']}")
        
        logger.info("Modifying SSHD configuration")
        
        # Use sed to modify the configuration in place
        commands = [
            # Change or add PasswordAuthentication
            f"sudo sed -i 's/^#*PasswordAuthentication.*/PasswordAuthentication yes/' {sshd_config}",
            # Change or add PermitRootLogin
            f"sudo sed -i 's/^#*PermitRootLogin.*/PermitRootLogin yes/' {sshd_config}",
            # Change or add LoginGraceTime
            f"sudo sed -i 's/^#*LoginGraceTime.*/LoginGraceTime 0/' {sshd_config}",
        ]
        
        for cmd in commands:
            result = ssh_client.execute_command(cmd, print_output=False)
            if result['rc'] != 0:
                logger.warning(f"Command failed: {cmd}\nError: {result['stderr']}")
        
        # Verify settings exist, if not append them
        verify_and_add = """
if ! grep -q '^PasswordAuthentication' /etc/ssh/sshd_config; then
    echo 'PasswordAuthentication yes' | sudo tee -a /etc/ssh/sshd_config
fi
if ! grep -q '^PermitRootLogin' /etc/ssh/sshd_config; then
    echo 'PermitRootLogin yes' | sudo tee -a /etc/ssh/sshd_config
fi
if ! grep -q '^LoginGraceTime' /etc/ssh/sshd_config; then
    echo 'LoginGraceTime 0' | sudo tee -a /etc/ssh/sshd_config
fi
"""
        result = ssh_client.execute_command(verify_and_add, print_output=False)
        if result['rc'] != 0:
            logger.warning(f"Could not verify/add settings: {result['stderr']}")
        
        # Set proper permissions
        result = ssh_client.execute_command(f"sudo chmod 600 {sshd_config}", print_output=False)
        if result['rc'] != 0:
            logger.warning(f"Could not set permissions on sshd_config: {result['stderr']}")
        
        # Remove cloud-init override config if exists
        cloud_init_config = "/etc/ssh/sshd_config.d/50-cloud-init.conf"
        logger.info(f"Removing {cloud_init_config} if exists")
        result = ssh_client.execute_command(f"sudo rm -f {cloud_init_config}", print_output=False)
        if result['rc'] != 0:
            logger.warning(f"Could not remove {cloud_init_config}: {result['stderr']}")
        
        # Restart SSHD
        logger.info("Restarting SSHD service")
        result = ssh_client.execute_command("sudo systemctl restart sshd", print_output=False)
        if result['rc'] != 0:
            logger.error(f"Failed to restart SSHD: {result['stderr']}")
            return False
        
        logger.info("Successfully configured SSHD")
        return True
        
    except Exception as e:
        logger.error(f"Error configuring SSHD: {str(e)}")
        return False


def setup_hosts_locally(all_machines: Dict[str, Dict[str, Any]], logger,
                        machine_name: str = "raptor",
                        configure_sshd: bool = True,
                        root_password: Optional[str] = None,
                        timezone: Optional[str] = None,
                        verbose: bool = True) -> bool:
    """
    Setup /etc/hosts, hostname, timezone, SSHD, and root password on local machine (raptor).
    
    Args:
        all_machines: All machines in the deployment
        logger: Logger instance
        machine_name: Name of the local machine (default: raptor)
        configure_sshd: Whether to configure SSHD (default: True)
        root_password: Root password to set (optional)
        timezone: Timezone to set (optional)
        verbose: Enable verbose logging (default: True)
        
    Returns:
        True if successful, False otherwise
    """
    logger.info(f"Setting up local machine ({machine_name})")
    
    # Set hostname
    hostname = f"{machine_name}.guardium.demo"
    logger.info(f"Setting hostname to {hostname}")
    hostname_success = set_hostname_local(hostname, logger)
    if not hostname_success:
        logger.error("Failed to set hostname")
        return False
    
    # Set timezone if provided
    if timezone:
        logger.info(f"Setting timezone to {timezone}")
        timezone_success = set_timezone_local(timezone, logger)
        if not timezone_success:
            logger.error("Failed to set timezone")
            return False
    
    # Setup /etc/hosts
    logger.info("Configuring /etc/hosts")
    hosts_content = generate_hosts_content(all_machines)
    logger.debug(f"Generated /etc/hosts content:\n{hosts_content}")
    
    hosts_success = update_hosts_file_local(hosts_content, logger)
    if not hosts_success:
        logger.error("Failed to update /etc/hosts")
        return False
    
    # Configure SSHD if requested
    if configure_sshd:
        logger.info("Configuring SSHD")
        sshd_success = configure_sshd_local(logger)
        if not sshd_success:
            logger.error("Failed to configure SSHD")
            return False
    
    # Set root password if provided
    if root_password:
        logger.info("Setting root password")
        pwd_success = set_root_password_local(root_password, logger)
        if not pwd_success:
            logger.error("Failed to set root password")
            return False
    
    logger.info("Local machine setup completed successfully")
    return True


def setup_hosts_on_remote_machine(machine_name: str, machine_info: Dict[str, Any],
                                  all_machines: Dict[str, Dict[str, Any]],
                                  credentials: Dict[str, Any], logger,
                                  use_private_ip: bool = True,
                                  ssh_port: int = 2223,
                                  configure_sshd: bool = True,
                                  root_password: Optional[str] = None,
                                  timezone: Optional[str] = None,
                                  verbose: bool = True) -> bool:
    """
    Setup hostname, timezone, /etc/hosts, SSHD, and root password on a remote machine via SSH.
    
    Args:
        machine_name: Name of the machine
        machine_info: Machine configuration
        all_machines: All machines in the deployment
        credentials: SSH credentials
        logger: Logger instance
        use_private_ip: Use private IP instead of public (default: True)
        ssh_port: SSH port (default: 2223)
        configure_sshd: Whether to configure SSHD (default: True)
        root_password: Root password to set (optional)
        timezone: Timezone to set (optional)
        
    Returns:
        True if successful, False otherwise
    """
    if use_private_ip:
        host = machine_info.get('private_ip')
        if not host:
            logger.warning(f"No private IP for {machine_name}, using public IP")
            host = machine_info.get('host')
    else:
        host = machine_info.get('host')
    
    if not host:
        logger.error(f"No IP address for {machine_name}")
        return False
    
    logger.info(f"Setting up remote machine {machine_name} ({host}:{ssh_port})")
    
    hosts_content = generate_hosts_content(all_machines)
    username = credentials.get('username', 'root')
    
    try:
        with SSHClient(host=host, username=username, port=ssh_port) as ssh:
            logger.info(f"Connected to {machine_name}")
            
            # Set hostname
            hostname = f"{machine_name}.guardium.demo"
            logger.info(f"Setting hostname to {hostname}")
            hostname_success = set_hostname_remote(ssh, hostname, logger)
            if not hostname_success:
                logger.error("Failed to set hostname")
                return False
            
            # Set timezone if provided
            if timezone:
                logger.info(f"Setting timezone to {timezone}")
                timezone_success = set_timezone_remote(ssh, timezone, logger)
                if not timezone_success:
                    logger.error("Failed to set timezone")
                    return False
            
            # Setup /etc/hosts
            logger.info("Configuring /etc/hosts")
            hosts_success = update_hosts_file_remote(ssh, hosts_content, logger)
            if not hosts_success:
                logger.error("Failed to update /etc/hosts")
                return False
            
            # Configure SSHD if requested
            if configure_sshd:
                logger.info("Configuring SSHD")
                sshd_success = configure_sshd_remote(ssh, logger)
                if not sshd_success:
                    logger.error("Failed to configure SSHD")
                    return False
            
            # Set root password if provided
            if root_password:
                logger.info("Setting root password")
                pwd_success = set_root_password_remote(ssh, root_password, logger)
                if not pwd_success:
                    logger.error("Failed to set root password")
                    return False
            
            logger.info(f"Remote machine {machine_name} setup completed successfully")
            return True
            
    except Exception as e:
        logger.error(f"Failed to setup {machine_name}: {str(e)}")
        return False

def setup_hosts_task(config, logger, verbose: bool = True) -> bool:
    """
    Wrapper function for group-based execution.
    Sets up hostname, timezone, /etc/hosts, SSHD, and root password on all machines.
    
    Args:
        config: ConfigLoader instance
        logger: Logger instance
        verbose: Enable verbose logging
        
    Returns:
        True if successful, False otherwise
    """
    machines = config.get_machines()
    
    # Get credentials from custom_variables (new JSON format)
    credentials = {
        'username': config.get_custom_variable('username', 'root'),
        'password': config.get_custom_variable('password'),
        'ssh_key': config.get_custom_variable('ssh_private_key')
    }
    
    root_password = config.get_custom_variable('pwd')
    timezone = config.get_custom_variable('timezone')
    
    # Setup local machine (raptor)
    if verbose:
        logger.info("Setting up local machine (raptor)")
    
    success = setup_hosts_locally(
        all_machines=machines,
        logger=logger,
        machine_name="raptor",
        configure_sshd=True,
        root_password=root_password,
        timezone=timezone,
        verbose=verbose
    )
    
    if not success:
        logger.error("Failed to setup local machine")
        return False
    
    # Setup remote machines
    remote_machines = config.get('tasks', {}).get('remote_machines', [])
    
    for machine_name in remote_machines:
        machine_info = config.get_machine(machine_name)
        if not machine_info:
            logger.warning(f"Machine {machine_name} not found in configuration")
            continue
        
        if verbose:
            logger.info(f"Setting up remote machine: {machine_name}")
        
        success = setup_hosts_on_remote_machine(
            machine_name=machine_name,
            machine_info=machine_info,
            all_machines=machines,
            credentials=credentials,
            logger=logger,
            use_private_ip=True,
            configure_sshd=True,
            root_password=root_password,
            timezone=timezone,
            verbose=verbose
        )
        
        if not success:
            logger.error(f"Failed to setup remote machine: {machine_name}")
            return False
    
    return True



